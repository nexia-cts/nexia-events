package com.nexia.teams.utilities.data;

public class SavedNexiaData {
    public boolean pvpEnabled;
    public boolean endDisabled;
    public boolean netherDisabled;
    public boolean hardcoreEnabled;

    public SavedNexiaData(boolean pvpEnabled, boolean endDisabled, boolean netherDisabled, boolean hardcoreEnabled) {
        this.pvpEnabled = pvpEnabled;
        this.endDisabled = endDisabled;
        this.netherDisabled = netherDisabled;
        this.hardcoreEnabled = hardcoreEnabled;
    }
}
